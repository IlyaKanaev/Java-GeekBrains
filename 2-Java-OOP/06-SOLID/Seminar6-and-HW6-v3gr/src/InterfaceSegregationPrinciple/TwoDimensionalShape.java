package InterfaceSegregationPrinciple;

/**
 * Интерфейс TwoDimensionalShape представляет собой интерфейс для двумерных фигур.
 * Содержит единственный метод area(), предназначенный для расчета площади фигуры.
 */
public interface TwoDimensionalShape {

    /**
     * Метод для расчета площади фигуры.
     * @return Площадь фигуры.
     */
    double area();
}
